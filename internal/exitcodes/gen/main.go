// main.go - Generate shell exit-code constants from the canonical Go contract.
//
// Purpose:
//   - Emit `scripts/libexec/exitcodes.sh` from `internal/exitcodes/exitcodes.go`.
//
// Responsibilities:
//   - Parse explicit `ACPExit*` constants from the Go source of truth.
//   - Convert the Go constant names into stable shell variable names.
//   - Write a generated shell include consumed by bridge scripts.
//
// Scope:
//   - Code generation only.
//
// Usage:
//   - Run via `go generate ./internal/exitcodes`.
//
// Invariants/Assumptions:
//   - Exit-code constants remain explicit numeric literals.
//   - Generated shell names stay in `ACP_EXIT_*` form.
package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"regexp"
	"strconv"
	"strings"
)

type exitCode struct {
	Name  string
	Value int
}

var camelBoundary = regexp.MustCompile(`([a-z0-9])([A-Z])`)

func main() {
	repoRoot, err := filepath.Abs(filepath.Join(".", "..", ".."))
	if err != nil {
		panic(err)
	}

	values, err := parseExitCodes(filepath.Join(repoRoot, "internal", "exitcodes", "exitcodes.go"))
	if err != nil {
		panic(err)
	}

	var buf bytes.Buffer
	buf.WriteString("#!/usr/bin/env bash\n")
	buf.WriteString("# Code generated by go generate ./internal/exitcodes. DO NOT EDIT.\n\n")

	readonlyNames := make([]string, 0, len(values))
	for _, item := range values {
		shellName := toShellName(item.Name)
		fmt.Fprintf(&buf, "%s=%d\n", shellName, item.Value)
		readonlyNames = append(readonlyNames, shellName)
	}
	fmt.Fprintf(&buf, "\nreadonly %s\n", strings.Join(readonlyNames, " "))

	outputPath := filepath.Join(repoRoot, "scripts", "libexec", "exitcodes.sh")
	if err := os.WriteFile(outputPath, buf.Bytes(), 0o644); err != nil {
		panic(err)
	}
}

func parseExitCodes(path string) ([]exitCode, error) {
	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, path, nil, 0)
	if err != nil {
		return nil, err
	}

	var values []exitCode
	for _, decl := range file.Decls {
		genDecl, ok := decl.(*ast.GenDecl)
		if !ok || genDecl.Tok != token.CONST {
			continue
		}
		for _, spec := range genDecl.Specs {
			valueSpec, ok := spec.(*ast.ValueSpec)
			if !ok {
				continue
			}
			for i, name := range valueSpec.Names {
				if !strings.HasPrefix(name.Name, "ACPExit") {
					continue
				}
				if i >= len(valueSpec.Values) {
					return nil, fmt.Errorf("constant %s is missing an explicit value", name.Name)
				}
				lit, ok := valueSpec.Values[i].(*ast.BasicLit)
				if !ok {
					return nil, fmt.Errorf("constant %s is not a basic literal", name.Name)
				}
				value, err := strconv.Atoi(lit.Value)
				if err != nil {
					return nil, fmt.Errorf("parse %s: %w", name.Name, err)
				}
				values = append(values, exitCode{Name: name.Name, Value: value})
			}
		}
	}

	return values, nil
}

func toShellName(goName string) string {
	base := strings.TrimPrefix(goName, "ACPExit")
	base = camelBoundary.ReplaceAllString(base, "${1}_${2}")
	return "ACP_EXIT_" + strings.ToUpper(base)
}
